  vaccination_info,
  health_safety_info,
  source,
  coords_lat,
  coords_lon
)
SELECT
  d.name,
  d.country,
  d.country_code,
  d.type,
  COALESCE(d.types, ARRAY[d.type]),
  d.image_url,
  d.description,
  d.highlights,
  d.best_season,
  d.average_daily_cost,
  d.currency,
  d.visa_info,
  d.vaccination_info,
  d.health_safety_info,
  d.source,
  d.coords_lat,
  d.coords_lon
FROM data d
WHERE NOT EXISTS (
  SELECT 1
  FROM public.destinations existing
  WHERE existing.type = 'country'
    AND existing.name = d.name
    AND existing.country = d.country
);

UPDATE public.destinations
SET image_url = 'https://flagcdn.com/w1280/' || lower(country_code) || '.png'
WHERE type = 'country'
  AND source = 'generated'
  AND country_code IS NOT NULL
  AND (
    image_url IS NULL
    OR image_url = ''
    OR image_url ILIKE 'https://picsum.photos/seed/%'
    OR image_url ILIKE 'https://source.unsplash.com/%'
  );
