SELECT
  d.name,
  d.country,
  d.country_code,
  d.type,
  COALESCE(d.types, ARRAY[d.type]),
  d.image_url,
  d.description,
  d.highlights,
  d.best_season,
  d.average_daily_cost,
  d.currency,
  d.visa_info,
  d.vaccination_info,
  d.health_safety_info,
  d.source,
  d.coords_lat,
  d.coords_lon
FROM data d
WHERE NOT EXISTS (
  SELECT 1
  FROM public.destinations existing
  WHERE existing.type = 'country'
    AND existing.name = d.name
    AND existing.country = d.country
);
